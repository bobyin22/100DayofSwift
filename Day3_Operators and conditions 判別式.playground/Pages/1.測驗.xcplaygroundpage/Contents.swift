import Foundation

//Which of these make result an integer equal to 5?
//Hint: The % operator calculates division remainder

//1️⃣
//✅ Option 1:
var result1 = 11 % 6
//11 divides into 6 once, leaving remainder 5.

//❌Option 2:
//var result1 = 2 + 2 + 1.0   //這樣會是Double，不是Int
var result1_1:Int = 2 + 2 + 1
//This will create a double, not an integer.


//2️⃣
//✅Option 1:
let result2 = 11 - 6
//11 minus 6 makes 5.

//❌Option 2:
//let result = 10 / 2.0
let result2_1 = 10 / 2
//This will create a double, not an integer.

//3️⃣
//✅Option 1:
var result3 = 7 - 1 - 1
//7 minus 1, minus another 1, makes five.

//❌Option 2:
var result3_1 = 12 - 8
//This will set result to 4.

//4️⃣
//✅Option 1:
var result4 = 1 * 5
//1 multiplied by 5 makes 5.

//❌Option 2:
//var result4_1 = 2 + 2 + 2
var result4_1 = 2 + 2 + 1
//This will set result to 6.

//5️⃣
//❌Option 1:
//let result = 12 % 6
//This will set result to be 0.

//✅Option 2:
let result = 20 / 4
//20 divided by 4 makes 5.

//6️⃣
//Option 1:
//let result6 = 2.5 * 2
//This will create a double, not an integer.

//Option 2:
let result6_1 = 5 + 0
//5 plus 0 makes 5.


